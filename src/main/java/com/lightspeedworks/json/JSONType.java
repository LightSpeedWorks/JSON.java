/**
 * JSON Type {JSON型}
 */
package com.lightspeedworks.json;

/**
 * JSON Type {JSON型}
 *
 * @author LightSpeedC (Kazuaki Nishizawa; 西澤 和晃)
 */
public enum JSONType {
	/**
	 * 数値型
	 */
	NUMBER,
	/**
	 * 文字列型
	 */
	STRING,
	/**
	 * ブーリアン型
	 */
	BOOLEAN,
	/**
	 * 配列型
	 */
	ARRAY,
	/**
	 * オブジェクト型
	 */
	OBJECT,
	/**
	 * ヌル型
	 */
	NULL;
}
